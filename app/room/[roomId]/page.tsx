    saveMessage({ sender_id: user?.name || "me", sender_name: user?.name || "Me", sender_type: "human", content: text.trim() });
    messagesRef.current = [...messagesRef.current, msg];
    setMessages(messagesRef.current);
    setTimeout(() => askAIs(messagesRef.current), 500);
