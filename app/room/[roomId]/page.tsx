    2: [[30, 22], [70, 22]],
    3: [[50, 14], [24, 50], [76, 50]],
    4: [[30, 14], [70, 14], [22, 52], [78, 52]],
    5: [[50, 10], [22, 30], [78, 30], [20, 58], [80, 58]],
