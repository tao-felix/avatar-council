    7: [[50, 8], [24, 22], [76, 22], [18, 44], [82, 44], [24, 64], [76, 64]],
    8: [[35, 8], [65, 8], [20, 28], [80, 28], [18, 50], [82, 50], [24, 66], [76, 66]],
